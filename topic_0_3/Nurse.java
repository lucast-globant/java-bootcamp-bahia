package topic_0_3;

public class Nurse {
	private String name;
	private int dni;
	public Nurse(String name, int dni) {
		this.name = name;
		this.dni = dni;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getDni() {
		return dni;
	}
	public void setDni(int dni) {
		this.dni = dni;
	}
}
